// Language Manager
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('alfaken_language') || 'pt';
        this.init();
    }
    
    init() {
        this.updatePageLanguage();
        this.setActiveLanguageButton();
    }
    
    changeLanguage(lang) {
        if (translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('alfaken_language', lang);
            this.updatePageLanguage();
            this.setActiveLanguageButton();
            
            // Update HTML lang attribute
            document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang === 'en' ? 'en-US' : 'es-ES';
        }
    }
    
    updatePageLanguage() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = translations[this.currentLanguage][key];
            
            if (translation) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation;
                } else if (element.tagName === 'OPTION') {
                    element.textContent = translation;
                } else {
                    element.innerHTML = translation;
                }
            }
        });
    }
    
    setActiveLanguageButton() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === this.currentLanguage) {
                btn.classList.add('active');
            }
        });
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

// Initialize Language Manager
let languageManager;
document.addEventListener('DOMContentLoaded', () => {
    languageManager = new LanguageManager();
});
